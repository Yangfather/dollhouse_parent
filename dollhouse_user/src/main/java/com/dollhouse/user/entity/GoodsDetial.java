package com.dollhouse.user.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

public class GoodsDetial implements Serializable {
	private static final long serialVersionUID = -4404664262030242701L;
	private String name;
	private Long id;
	private String URL;
	private BigDecimal lowerPrice;
	private String color;
	private String type;
	private BigDecimal price;
	private List<GoodsDetial> colors;
	private List<GoodsDetial> imgUrl;
	private String organCode;
//	type 里面
	private List<GoodsDetial> types;
	private List<GoodsDetial> prices;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getURL() {
		return URL;
	}
	public void setURL(String uRL) {
		URL = uRL;
	}
	public BigDecimal getLowerPrice() {
		return lowerPrice;
	}
	public void setLowerPrice(BigDecimal lowerPrice) {
		this.lowerPrice = lowerPrice;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public BigDecimal getPrice() {
		return price;
	}
	public void setPrice(BigDecimal price) {
		this.price = price;
	}
	public List<GoodsDetial> getColors() {
		return colors;
	}
	public void setColors(List<GoodsDetial> colors) {
		this.colors = colors;
	}
	public List<GoodsDetial> getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(List<GoodsDetial> imgUrl) {
		this.imgUrl = imgUrl;
	}
	public List<GoodsDetial> getTypes() {
		return types;
	}
	public void setTypes(List<GoodsDetial> types) {
		this.types = types;
	}
	public List<GoodsDetial> getPrices() {
		return prices;
	}
	public void setPrices(List<GoodsDetial> prices) {
		this.prices = prices;
	}
	public String getOrganCode() {
		return organCode;
	}
	public void setOrganCode(String organCode) {
		this.organCode = organCode;
	}
}
